package week1.day2;

public class Calculator {

	public int add(int a, int b, int c) {
		return a + b + c;

	}

	public int sub(int a, int b) {
		return a - b;
	}

	public double mul(double a, double b) {
		return a * b;
	}

	public float div(float a, float b) {
		return a / b;
	}

}
